#include "doom.h"

t_line			setline(t_fvector2d a1, t_fvector2d a2,
t_fvector2d b1, t_fvector2d b2)
{
	t_line l;

	l.p[0] = a1;
	l.p[1] = a2;
	l.p[2] = b1;
	l.p[3] = b2;
	return (l);
}
